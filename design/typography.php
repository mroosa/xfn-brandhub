            <h2>Price Points &amp; Sidebars</h2>
            <p>Xfinity Red bars can be used to call attention to an offer if needed. Offers and price points should be left aligned. Do not mix copy alignments.</p>
            <p>In most cases we do not include package names in our price points (eg: digital, TV). Only in Direct Marketing do we include package names. In these instances, Xfinity Brown Bold should be used for package names with services in Xfinity Brown Regular.</p>
