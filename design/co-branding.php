          <h1>Co-Branding</h1>
          <div class="section">
            <p>For key partnerships, please follow best practices for co-branded lock ups, ensuring appropriate distance and 1:1 size relationship between logos.</p>
            <p>For relationships where Xfinity is endorsing a partnership or experience, use a descriptive approach using the Xfinity logo. While endorsement line and sizing may vary, please ensure the Xfinity logo is legible and has impact.</p>

            <h3>Partnership Lock-ups</h3>
            <p>Co-branding lockups will always be horizontal. Co-brand logos should be the character height of the “n” in the Xfinity logo. In certain circumstances (i.e.: circular logos, etc.) it can be sized larger, but should not exceed the height of the “xf” in the Xfinity logo.</p>
            <p>A divider rule is placed between the logos and should not exceed the height of the “xf” in the Xfinity logo. Spacing between the logos and divider should be 50% of the character height “n”.</p>
            <p>To ensure prominence and legibility, co-branding lock-ups should be surrounded by an area of minimum clear space 50% of the character height “n” which should remain free of any type or graphic elements.</p>

            <h3>Descriptive Relationships</h3>
            <p>Text placed next to the logo should always be sentence case. To determine text size, lowercase characters in text should be 25% the character height of “n” in the Xfinity logo. Space between the text and logo should be 25% the character height of “n” in the Xfinity logo.</p>

            <ul>
              <li>Do not make partner logo same size as logo. Use 2:1 ratio only.</li>
              <li>Do not use sub-brands with partner logo lockups.</li>
              <li>Do not use Xfinity Future of Awesome logo with partner logos.</li>
              <li>Do not create new logos with logo and partner logo.</li>
            </ul>
          </div>

